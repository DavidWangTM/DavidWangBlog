                    <h2>花 999 元去看清一个残酷的事实？WeGene 发布基因检测套件帮你了解祖源和天赋</h2>
                    <div class="details">
                        <div class="info">
                            <img src="http://7xi85d.com2.z0.glb.qiniucdn.com/uploads%2Fusers%2Favatar%2F1073135091%2Fthumb_Jacky__.jpg" alt="Uploads%2fusers%2favatar%2f1073135091%2fthumb jacky  ">
                            <a href="/users/1073135091">Jacky.ac</a>
                            <span>发布于2015年10月26日</span>
                        </div>
                        <div class="cont">
                            <p>基因检测在以前似乎是很玄乎的技术，毕竟基因是决定一个人健康、外貌、智慧的根本，掌握了基因的情况，基本就知道自己的能耐去到哪儿了。</p><p>2000 年一个基因组的测序成本约为一亿美元，后来随着基因芯片出现，基因检测费用才降到千元级别。目前业内规模最大的消费级基因检测公司 23andMe 的收费单次是 199 美元，今天，深圳 WeGene 公司发布了一组基因检测套件，价格 999 元。</p><div class="medium-insert-images"><figure contenteditable="false">
                                    <img src="http://7xi85d.com2.z0.glb.qiniucdn.com/uploads%2Fpicture%2Ffile%2F4394%2Flarge_Screen_Shot_2015-10-26_at_8.08.18_PM.png" alt="" data-id="4394">
                                </figure></div><h3><i>WeGene 可以做哪些检测？</i></h3><p>WeGene 的实例报告中这么写：</p><p>51.62% 南方汉族血统；</p><p>父系祖源 O3a1c1，起源于 28,000 - 41,000 年前</p><p>母系祖源 M10a1a，起源于 60,000 年前</p><p>O 型血、运动有助于减肥、记忆力不错、吸烟不容易上瘾、喝酒不脸红</p><p>上面这些项目，包括祖源分析、运动基因、健康风险、药物反应等。另外 WeGene 还可以帮你检测遗传特征、遗传性疾病等，共计 300 多项检测结果。<span style="font-size: 14px; line-height: 17.2817287445068px;">WeGene 的基因芯片可以检测 60 万个突变位点，是目前针对中国人点位覆盖率最高的。</span></p><p>有些人喝口水都会胖，有些人很容易就减肥成功，基因检测都可以告诉你原因。大概的意思就是，做一次基因检测，你的前世今生基本都了解了！</p><div class="medium-insert-images"><figure contenteditable="false">
                                    <img src="http://7xi85d.com2.z0.glb.qiniucdn.com/uploads%2Fpicture%2Ffile%2F4395%2Flarge_____.jpg" alt="" data-id="4395">
                                </figure></div><p>和 23andMe 的检测流程类似，WeGene 的检测流程也是网上购买－绑定账号（收到采集工具后）－采集－回寄－查看报告这样一种「足不出户」的做法。一般情况下，用户可以在 60 后内收到完整检测报告，在这之前可以登录账号查看检测进度。</p><h3><i>基因检测靠谱吗？</i></h3><p>2013 年的时候，23andMe 曾被美国 FDA（US Food and Drug Administration）禁止出具带有诊断性的健康报告，不过日前 23andMe 又宣布他们重新获得了 FDA 的批准。</p><p>因为当前生命科学研究的局限性，数据库垄断、分析方法不统一，以及遗传咨询的缺失等问题，通过对特定 SNP 的检测来对用户患病概率作出评估，实际上是不严谨的。所以 23andMe 此次只是获批进行祖源、健康、非医学特征以及疾病携带状况的信息报告，仍不被允许向消费者提供一些疾病的风险预测分析和对相应药物的过敏分析，全面放开还需要很长一段时间。</p><p class="">WeGene 的检测结果也不具备临床意义，但作为健康指导就是绰绰有余的。</p><p>另外，基因检测还有心理层面的风险。比如家长给小孩做了基因检测后，发现小孩得某种癌症的概率很高，家长会变得忧心忡忡，小孩子也可能对未来失去信心。而基因的作用都清楚，所以一份薄薄的基因检测报告，有时候可能会变成沉甸甸的负担。WeGene 考虑到这个问题后的做法是，需要签知情同意书，才能看疾病先相关的检测结果。</p><h3><i>基因检测和定制</i></h3><p>因为每个人的代谢效率、无氧运动能力、耐力、恢复能力等各项体质特征不同，WeGene 推出了一个和 inWatch 智能手表的合作计划：根据个人基因检测数据，推荐更加科学的运动和锻炼方案。这个合作计划正是基于 WeGene 的开放平台开发的：让更专业的人来使用底层基因数据。</p><p>未来我们可能会看到更多私人定制，哦不对，是基因定制的产品，比如基因定制午餐、基因定制晚餐、基因定制职业发展规划、基因定制伴侣等等。嗯，想想还是挺科幻的。</p></div>
