                                </figure></div><p>有潮水的地方就有潮汕人，有钱赚的地方就有潮商。潮汕商人与晋商、徽商合称为中国传统三大商帮，经过 500 多年的商业洗礼，潮商已经成为中国实力最大、影响最深远、唯一没有断代的大商帮，是华人世界中最富有的族群。</p><p>「有出息的男人不会待在家里」，潮汕人天生为了经商而存在。作为南部沿海文化聚集地的汕头，孕育了一大批国内外知名食品、化妆品、玩具等企业。</p><p>此次「深圳湾」特色游，将特邀创客创业导师程天纵和潮汕游子共赴汕头，与潮汕企业家、创业者、学子共话创业浓情。</p><h3><b>行程安排</b></h3><p><b>11 月 6 日</b></p><p><b> 14: 30~17: 30 50 后<span style="font-size: 14px;">创业导师对话 80 后</span><span style="font-size: 14px;">潮人创业家</span></b><b style="font-family: inherit; font-style: inherit;">（汕头大学&nbsp;</b><span style="font-size: 14px;"><b>图书馆报告厅</b></span><b style="font-family: inherit; font-style: inherit;">）</b></p><p><b>活动议程：</b></p><p>14: 00~14: 30 活动签到</p><p>14: 30~14: 40 汕大李润钿老师开场致辞</p><p>14: 40~15: 30 就业与创业 程天纵</p><p>15: 30~16: 00 创业 -- 修行之路！李诺夫</p><p>16: 00~16: 30&nbsp;儿童机器人创业，追逐遥不可及的梦想！&nbsp;黄金龙</p><p>16: 30~17: 10 圆桌讨论</p><p>17: 10~17: 30 问答环节</p><p><b>[ 就业与创业 ]&nbsp;</b></p><div class="medium-insert-images"><span style="font-size: 14px;"></span><figure contenteditable="false">
                                    <img src="http://7xi85d.com2.z0.glb.qiniucdn.com/uploads%2Fpicture%2Ffile%2F4389%2Flarge_33.pic_hd.jpg" alt="" data-id="4389">
                                </figure></div><p><b>程天纵：Terry &amp; Friends 创办人，原中国惠普总裁、德州仪器亚太区总裁、富士康集团副总裁</b></p><p>从&nbsp;1979&nbsp;年走进惠普台湾分公司开疆辟土，到惠普香港分公司、惠普硅谷分公司开始走上争权夺利的道路，然后在中国惠普担任第三任总裁期间学会了有容乃大，再到德州仪器深谙跨国公司的中国策略和全球营销，最后从富士康累到退休。程天纵老师这一生的职业经理人经历，有如一本厚厚的职业生涯指南，充满了智慧。 </p><p>主题分享内容非常适合在校毕业生，对于大家接下来的求职或创业都有深远的指导意义，希望大家能够把握这次机会和这些些宝贵的知识财富。&nbsp;</p><p><b>[&nbsp;创业 -- 修行之路！]&nbsp;</b></p><div class="medium-insert-images"><span style="font-size: 14px;"></span><figure contenteditable="false">
                                    <img src="http://7xi85d.com2.z0.glb.qiniucdn.com/uploads%2Fpicture%2Ffile%2F4388%2Flarge_WeChat_1445831449.jpeg" alt="" data-id="4388" class="">
                                </figure></div><p><b>李诺夫：华强北国际创客中心总经理</b></p><p>我很早就走上了创业的道路，做过牛奶代理商、电脑绣花、开宾馆和物流公司。在 2004 年，当我看到液晶屏的发展潜力和趋势时，果断做起了液晶屏的代理商，这也让我进入到电子行业一直到现在。在电子行业摸爬滚打多年后，对电子以及硬件行业也有了自己的理解。在此过程，还尝试过用互联网思维改变茶行业，创造性的推出了风靡的子曰茶。 </p><p>而如今，身为 80 后的我现投身于创新创业综合生态平台，目前是华强北国际创客中心总经理。创业多年，始终如一，每一次经历，每一次感悟，都让我愈加愈加珍惜、享受！&nbsp;</p><p><b>[ 儿童机器人创业，追逐遥不可及的梦想！ ]&nbsp;</b></p><div class="medium-insert-images"><span style="font-size: 14px;"></span><figure contenteditable="false">
                                    <img src="http://7xi85d.com2.z0.glb.qiniucdn.com/uploads%2Fpicture%2Ffile%2F4387%2Flarge_32.pic_hd.jpg" alt="" data-id="4387">
                                </figure></div><p><b>黄金龙：小西科技创始人，T&amp;F 潮汕群群主，T&amp;F 营销群群主</b></p><p>一个技术和创业老兵，从&nbsp;2002&nbsp;年开始就做硬件和软件开发，2006&nbsp;年开始创办广州惟易科技，2008&nbsp;年创办物流联合网&nbsp;，2010年继续投入手机和3C数码配件创业…&nbsp; </p><p>在这个过程中，有过成功也有过失败，但创业的乐趣也正在于此。我是一个典型的连续创业者，现在我选择重新起航，投身智能硬件。至于为什么选择儿童机器人创业？儿童机器人创业过程经历？以及我对智能硬件的思考？期待你的聆听。&nbsp;</p><p><b>19: 00~22: 00 浓情潮汕游子聚会</b></p><p>作为此次汕头行重要温情环节，潮汕游子将在故乡的土地上相聚，共话情怀。美酒美食相伴，共度一个美好的夜晚。</p><div class="medium-insert-images"><figure contenteditable="false">
                                    <img src="http://7xi85d.com2.z0.glb.qiniucdn.com/uploads%2Fpicture%2Ffile%2F4429%2Flarge____-5.png" alt="" data-id="4429">
                                </figure></div><p><b>11 月 7 日</b></p><p><b>潮汕名企拜访</b></p><p>一天的时间，我们将会对汕头 3~4 家知名企业参观以及深度交流，感受汕头潮汕商帮文化。</p><div class="medium-insert-images"><figure contenteditable="false">
                                    <img src="http://7xi85d.com2.z0.glb.qiniucdn.com/uploads%2Fpicture%2Ffile%2F4430%2Flarge_shangbang.png" alt="" data-id="4430">
                                </figure></div><h3><b>费用说明</b></h3><p><b>800 元档费用包含（深圳小伙伴随团前往）：</b></p><p>1. 高铁费（来回深圳潮汕的高铁费用）</p><p>2. 接送巴士费（往返火车站以及行程中所需路程使用）</p><p>3. 餐费（午饭 2 顿、早餐 1 顿）</p><p>4. 聚餐（6 号晚，聚餐+酒水+茶点费用）</p><p>5. 住宿（标间双人床房）</p><p class=""><b>300 元档费用包含（限汕头本地小伙伴）：</b></p><p>1.&nbsp;聚餐（6 号晚，聚餐+酒水+茶点费用）</p><p>2.&nbsp;接送巴士费（7 号行程中所需路程使用）</p><p>3.&nbsp;餐费（午饭 1 顿）</p><h3><b>主办方</b></h3><p>深圳湾</p><p>T&amp;F 潮汕群</p><h3><b>协办方</b></h3><p>赋西资本</p><p>汕头大学</p><p>汕头市青年企业家协会</p><h3>合作伙伴</h3><p class="">华强北国际创客中心</p><p class="">深圳小西科技有限公司</p>
